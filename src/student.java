    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try {
            stmt = conn.createStatement();
            int  stdMatricule = Integer.parseInt(txtMatricule.getText());
        
            String sql = "SELECT * FROM STUDENT WHERE matricule = '"+stdMatricule+"'";
            
            rs = stmt.executeQuery(sql);
            
            if(rs.next()){
                txtNom.setText(rs.getString("nom"));
                txtPrenom.setText(rs.getString("prenom"));
                txtSexe.setSelectedItem(rs.getString("sexe"));
                txtCommune.setSelectedItem(rs.getString("commune"));
                txtTel.setText(rs.getString("tel"));
                
            }else{
                JOptionPane.showMessageDialog(null, "Ce matricule n'existe pas !!!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

