    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            stmt = conn.createStatement();
            Integer stdMatricule = Integer.parseInt(txtMatricule.getText());
            String stdNom = txtNom.getText();
            String stdPrenom = txtPrenom.getText();
            String stdSexe = txtSexe.getSelectedItem().toString();
            String stdCommune = txtCommune.getSelectedItem().toString();
            String stdTel = txtTel.getText();

            String sql = "UPDATE STUDENT SET nom = '"+stdNom+"', prenom ='"+stdPrenom+"', sexe = '"+stdSexe+"', commune = '"+stdCommune +"', tel = '"+stdTel+"' WHERE matricule= '"+stdMatricule+"'";

            stmt.executeUpdate(sql);

            JOptionPane.showMessageDialog(null, "Etudiant Modifier avec Succès ");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

